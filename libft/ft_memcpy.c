#include "libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		((int *)dest)[i] = ((const int *)src)[i];
		i++;
	}
	return (dest);
}
